public class ArrayBasictable {
    public static void main(String[] args) {
        System.out.println("Index \t Value");
        int James []={32,12,18,54,2,5,6,78,9,};

        for(int counter=0;counter<James.length;counter ++){
            System.out.println(counter+ "\t"+ James[counter]);
        }
    }
}
